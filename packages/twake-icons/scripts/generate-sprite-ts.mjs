#!/usr/bin/env node
import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const packageDir = join(__dirname, '..');
const spritePath = '/tmp/twake-icons-sprite.svg';
const outFile = join(packageDir, 'src/Icon/icons-sprite.ts');

const svgContent = readFileSync(spritePath, 'utf-8');

// Escape for template literal: backslashes first, then backticks, then ${
const escaped = svgContent
  .replace(/\\/g, '\\\\')
  .replace(/`/g, '\\`')
  .replace(/\$\{/g, '\\${');

const header = '// GENERATED FILE, DO NOT EDIT THIS FILE BY HAND\n// Use npm run sprite to regenerate\nexport default `' + escaped + '`\n';

writeFileSync(outFile, header, 'utf-8');
console.log(`Done: ${outFile}`);
